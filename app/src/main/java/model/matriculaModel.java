package model;

public class matriculaModel {
    public String matricula;
    public String nome;
    public String email;
    public String estado;
    public String cidade;

    public matriculaModel(String m, String n, String e, String est, String cid){
        matricula = m;
        nome =  n;
        email = e;
        estado = est;
        cidade = cid;
    }
}
